    public void CargarTabla()
    {
        int fila = this.jTable1.getRowCount();
        for (int i = 0; i < fila; i++) 
        {
            modelo.removeRow(0);
        }
        
        ArrayList<Empleado> arreglo = hotel.getEmpleados();
        String[] datos = new String[4];
        for (int i = 0; i < arreglo.size(); i++) 
        {
            datos[0] = arreglo.get(i).getRol();
            datos[1] = arreglo.get(i).getNombre();
            datos[2] = arreglo.get(i).getApellido();
            datos[3] = String.valueOf(arreglo.get(i).getDni());
            modelo.addRow(datos);
        }
    }
    
