    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        int error = 0;
        int numReserv = Integer.parseInt(this.jTextField6.getText());
        for (int i = 0; i < hotel.getReservaciones().size(); i++) 
        {
            Reservacion reserva = hotel.getReservaciones().get(i);
            if(reserva.getNumeroR()==numReserv)
            {
                error=1;
                Recep.CheckOut(hotel, hotel.getReservaciones().get(i));
                reserva.getEstadia().CalcularPrecioTotalNoches(reserva);
                reserva.getEstadia().CalcularPrecioConsumoServ();
                reserva.getEstadia().CalcularPrecioTotal();
                CargarTablaHabitacion();
                CargarTablaReservacion();
            }
        }
        if(error==0)
        {
            String texto = "Reserva inexistente";
            ErrorG e = new ErrorG(texto);
            e.setVisible(true);
        }
        

        
    }//GEN-LAST:event_jButton1ActionPerformed

