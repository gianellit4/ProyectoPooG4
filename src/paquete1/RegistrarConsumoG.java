    public void CargarTablaServicios()
    {
        int fila = this.jTable1.getRowCount();
        for (int i = 0; i < fila; i++) 
        {
            modelo1.removeRow(0);
        }

        ArrayList<ServicioAdicional> arreglo = hotel.getServicios();
        String[] datos = new String[2];
        for (int i = 0; i < arreglo.size(); i++) 
        {   
            datos[0] = arreglo.get(i).getNombreServAdi();
            datos[1] = String.valueOf(arreglo.get(i).getPrecioServAdi());
            modelo1.addRow(datos);
        }
    }
    
    public void CargarTablaReservacion()
    {
        int fila = this.jTable2.getRowCount();
        for (int i = 0; i < fila; i++) 
        {
            modelo2.removeRow(0);
        }
        
        ArrayList<Reservacion> arreglo = hotel.getReservaciones();
        String[] datos = new String[5];
        for (int i = 0; i < arreglo.size(); i++) 
        {
            datos[0] = String.valueOf(arreglo.get(i).getNumeroR());
            datos[1] = String.valueOf(arreglo.get(i).getNoches());
            datos[2] = arreglo.get(i).getTipoHabitacion();
            datos[3] = String.valueOf(arreglo.get(i).getNumeroPersonas());
            if(arreglo.get(i).getHuespedesDeReservacion().size()==1) //PONER EN BASE A SIZE DEL ARREGLO
            {
                datos[4] = arreglo.get(i).getHuespedesDeReservacion().get(0).getNombreHuesped();
            }
            if(arreglo.get(i).getHuespedesDeReservacion().size()==2)
            {
                datos[4] = arreglo.get(i).getHuespedesDeReservacion().get(0).getNombreHuesped()+" y "+
                       arreglo.get(i).getHuespedesDeReservacion().get(1).getNombreHuesped();
            }
            //Faltan los huespedes
            modelo2.addRow(datos);
        }
    }
    
    public void CargarTablaConsumos()
    {
        modelo3 = new DefaultTableModel();
        this.jTable3.setModel(modelo3);

        //BORRAR DATOS
        int fila = this.jTable3.getRowCount();
        for (int i = 0; i < fila; i++) 
        {
                modelo3.removeRow(0);
        }
        
        
        //EL RESTO
        ArrayList<ServicioAdicional> arreglo = hotel.getServicios();
        int cantidad = arreglo.size();
        
        modelo3.addColumn("Numero de reserva");
        for (int i = 0; i < cantidad; i++) 
        {
            String servicio = arreglo.get(i).getNombreServAdi();
            modelo3.addColumn(servicio);
        }
        this.jTable3.setModel(modelo3);
        

        
        String[] datos = new String[cantidad+1];
        for (int i = 0; i < hotel.getReservaciones().size(); i++) 
        {   
            datos[0] = String.valueOf(hotel.getReservaciones().get(i).getNumeroR());
            for (int j = 1; j < cantidad+1; j++) 
            {
                int cant = Recep.CantidadServicios(hotel, hotel.getReservaciones().get(i), hotel.getServicios().get(j-1).getNombreServAdi());
                datos[j] = String.valueOf(cant);
            }
        }
        modelo3.addRow(datos);
    }
    
    
    public void CargarTablaConsumos2()
{
    // Limpia modelo manteniendo columnas
    DefaultTableModel modelo3 = new DefaultTableModel();
    
    ArrayList<ServicioAdicional> arreglo = hotel.getServicios();
    int cantidad = arreglo.size();

    // Crear columnas
    modelo3.addColumn("Numero de reserva");
    for (ServicioAdicional s : arreglo) {
        modelo3.addColumn(s.getNombreServAdi());
    }

    // Agregar filas
    for (Reservacion r : hotel.getReservaciones()) {

        String[] datos = new String[cantidad + 1];

        datos[0] = String.valueOf(r.getNumeroR());

        for (int j = 0; j < cantidad; j++) {
            int cant = Recep.CantidadServicios(hotel, r, arreglo.get(j).getNombreServAdi());
            datos[j + 1] = String.valueOf(cant);
        }

        modelo3.addRow(datos);
    }

    jTable3.setModel(modelo3);
}

    
    
