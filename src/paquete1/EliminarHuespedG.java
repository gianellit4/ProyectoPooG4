    public void CargarTabla()
    {
        int fila = this.jTable1.getRowCount();
        for (int i = 0; i < fila; i++) 
        {
            modelo.removeRow(0);
        }
        
        ArrayList<Huesped> arreglo = hotel.getHuespedes();
        String[] datos = new String[4];
        for (int i = 0; i < arreglo.size(); i++) 
        {
            datos[0] = String.valueOf(arreglo.get(i).getDNIhuesped());
            datos[1] = arreglo.get(i).getNombreHuesped();
            datos[2] = arreglo.get(i).getApellidoHuesped();
            datos[3] = arreglo.get(i).getDatosContacto();
            modelo.addRow(datos);
        }
    }
    
