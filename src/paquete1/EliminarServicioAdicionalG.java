    public void CargarTabla()
    {
        int fila = this.jTable1.getRowCount();
        for (int i = 0; i < fila; i++) 
        {
            modelo.removeRow(0);
        }
        
        ArrayList<ServicioAdicional> arreglo = hotel.getServicios();
        String[] datos = new String[2];
        for (int i = 0; i < arreglo.size(); i++) 
        {
            datos[0] = arreglo.get(i).getNombreServAdi();
            datos[1] = String.valueOf(arreglo.get(i).getPrecioServAdi());
            modelo.addRow(datos);
        }
    }
    
