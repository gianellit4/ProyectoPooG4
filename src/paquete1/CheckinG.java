    public void CargarTablaReservacion()
    {
        int fila = this.jTable1.getRowCount();
        for (int i = 0; i < fila; i++) 
        {
            modelo1.removeRow(0);
        }
        
        ArrayList<Reservacion> arreglo = hotel.getReservaciones();
        String[] datos = new String[4];
        for (int i = 0; i < arreglo.size(); i++) 
        {
            datos[0] = String.valueOf(arreglo.get(i).getNumeroR());
            
            if(arreglo.get(i).getHuespedesDeReservacion().size()==2)
            {
                datos[1] = arreglo.get(i).getHuespedesDeReservacion().get(0).getNombreHuesped()+" y "+
                        arreglo.get(i).getHuespedesDeReservacion().get(1).getNombreHuesped();
            }
            else
            {
                datos[1] = arreglo.get(i).getHuespedesDeReservacion().get(0).getNombreHuesped();
            }
            
            datos[2] = String.valueOf(arreglo.get(i).getNoches());
            datos[3] = arreglo.get(i).getTipoHabitacion();
            modelo1.addRow(datos);
        }
    }
    
        public void CargarTablaAsignacion()
    {
        int fila = this.jTable2.getRowCount();
        for (int i = 0; i < fila; i++) 
        {
                modelo2.removeRow(0);
                    
        }
        
        ArrayList<Reservacion> arreglo = hotel.getReservaciones();
        String[] datos = new String[4];
        int cont =0;
        for (int i = 0; i < arreglo.size(); i++) 
        {
            if(arreglo.get(i).getHabitacion()!=null)
            {
                cont=cont+1;
            }
        }
        
        for (int i = 0; i < arreglo.size(); i++) 
        {
            if(arreglo.get(i).getHabitacion()==null)
            {
                continue;
            }
            
            datos[0] = String.valueOf(arreglo.get(i).getNumeroR());
            if(arreglo.get(i).getHuespedesDeReservacion().size()==2)
            {
                datos[1] = arreglo.get(i).getHuespedesDeReservacion().get(0).getNombreHuesped()+" y "+
                        arreglo.get(i).getHuespedesDeReservacion().get(1).getNombreHuesped();
            }
            else
            {
                datos[1] = arreglo.get(i).getHuespedesDeReservacion().get(0).getNombreHuesped();
            }
            
            datos[2] = String.valueOf(arreglo.get(i).getHabitacion().getNumero());
            System.out.println("HABITACION NO ES NULO");
            datos[3] = arreglo.get(i).getTipoHabitacion();
            modelo2.addRow(datos);
        }
    }
    
