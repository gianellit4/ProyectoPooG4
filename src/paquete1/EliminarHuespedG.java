    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        //ELIMINAR OBJETO
        String nombre = this.jTextField7.getText();
        String apellido = this.jTextField4.getText();
        Huesped hu = null;
        for (int i = 0; i < hotel.getHuespedes().size(); i++) 
        {
            if(hotel.getHuespedes().get(i).getNombreHuesped().equals(nombre) && hotel.getHuespedes().get(i).getApellidoHuesped().equals(apellido))
            {
                hu = hotel.getHuespedes().get(i);
                Recep.eliminarHuesped(hotel, hu);
                CargarTabla();
            }
        }
        if(hu==null)
        {
            String texto = "No existe un huesped con ese nombre y apellido";
            ErrorG e = new ErrorG(texto);
            e.setVisible(true);
        }

        
    }//GEN-LAST:event_jButton1ActionPerformed

