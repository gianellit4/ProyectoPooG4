    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String nombre = this.jTextField6.getText();
        ServicioAdicional serv = null;
        for (int i = 0; i < hotel.getServicios().size(); i++) 
        {
            if(hotel.getServicios().get(i).getNombreServAdi().equals(nombre))
            {
                serv = hotel.getServicios().get(i);
                Adm.eliminarServicio(hotel, serv);
                CargarTabla();
                return;
            }
        }
        String texto = "No hay un servicio con ese nombre";
        ErrorG e = new ErrorG(texto);
        e.setVisible(true);

        
    }//GEN-LAST:event_jButton1ActionPerformed

